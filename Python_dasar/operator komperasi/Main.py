# mengenal operator komperasi dengan user input

# operator >
print("Operator > ")
a = int(input("Masukkan angka[1]: "))
b = int(input("Masukkan angka[2]: "))
hasil = a > b 
print(a, ">", b, "=", hasil )
print("")

